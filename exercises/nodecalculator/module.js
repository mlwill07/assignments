module.exports.add = function(num1, num2) {
    return num1 + num2;
}

module.exports.sub = function(num1, num2) {
    return num1 - num2;
}

module.exports.div = function(num1, num2) {
    return num1 / num2;
}

module.exports.mult = function(num1, num2) {
    return num1 * num2;
}

module.exports.expo = function(num1, num2) {
    return Math.pow(num1, num2);
}